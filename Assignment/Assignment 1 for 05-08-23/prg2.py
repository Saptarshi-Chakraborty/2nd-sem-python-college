'''
    CELCIUS TO FARENHEIT
     c * (9 / 5) + 32
'''

print("\n\tCELCIUS TO FARENHEIT CONVERSTION\n")

c = float(input("Enter a temperature in celcius : "))
f = c * (9/5) + 32
print("\n>> {0} degree celcius = {1} degree farenheit".format(c, f))


