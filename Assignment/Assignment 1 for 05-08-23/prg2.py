'''
    CELSIUS TO FAHRENHEIT
     c * (9 / 5) + 32
'''

print("\n\tCELSIUS TO FAHRENHEIT CONVERSTION\n")

c = float(input("Enter a temperature in celcius : "))
f = c * (9/5) + 32
print("\n>> {0} degree celsius = {1} degree fahrenheit".format(c, f))


